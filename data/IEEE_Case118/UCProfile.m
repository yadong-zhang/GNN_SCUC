function profile = UCProfile(UC_sample, thermal_gen_no)

% define constants
[CT_LABEL, CT_PROB, CT_TABLE, CT_TBUS, CT_TGEN, CT_TBRCH, CT_TAREABUS, ...
    CT_TAREAGEN, CT_TAREABRCH, CT_ROW, CT_COL, CT_CHGTYPE, CT_REP, ...
    CT_REL, CT_ADD, CT_NEWVAL, CT_TLOAD, CT_TAREALOAD, CT_LOAD_ALL_PQ, ...
    CT_LOAD_FIX_PQ, CT_LOAD_DIS_PQ, CT_LOAD_ALL_P, CT_LOAD_FIX_P, ...
    CT_LOAD_DIS_P, CT_TGENCOST, CT_TAREAGENCOST, CT_MODCOST_F, ...
    CT_MODCOST_X] = idx_ct;

profile = struct( ...
    'type', 'xGenData', ...
    'table', 'CommitKey', ...
    'rows', [1;3;4], ...  % for generators 1, 3 and 4
    'col', 0, ...
    'chgtype', CT_REP, ...
    'values', [] );


nt = size(UC_sample, 1);

num_thermal_gens = size(UC_sample, 2);


profile.rows = reshape(thermal_gen_no, [1, num_thermal_gens]);

profile.values = reshape(UC_sample, [nt, 1, num_thermal_gens]);


end